class DemografiaEstatalExtractor(BaseExtractor):
    periodicidad = "quinquenal"
    schema = "anual"
    tabla = "datos"
    indicador_clave = INDICADOR_CLAVE
    fuente_id: int = 0

    def extract(self) -> list[dict[str, Any]]:
        logger.info("Cargando demografía estatal (INEGI)")
        return [
            {
                "estado": e,
                "anio": a,
                "total": t,
                "hombres": h,
                "mujeres": m,
                "j": j,
                "ad": ad,
                "may": ma,
            }
            for e, a, t, h, m, j, ad, ma in DATOS + DATOS_2 + DATOS_3
        ]

    def transform(self, raw: list[dict[str, Any]]) -> list[dict[str, Any]]:
        records = []
        for row in raw:
            edo = row["estado"]
            anio = row["anio"]
            for metric, val in [
                (f"total:{edo}", row["total"]),
                (f"hombres:{edo}", row["hombres"]),
                (f"mujeres:{edo}", row["mujeres"]),
                (f"0-19:{edo}", row["j"]),
                (f"20-64:{edo}", row["ad"]),
                (f"65+:{edo}", row["may"]),
            ]:
                records.append(
                    {
                        "indicador_id": None,
                        "nivel_geografico": "estatal",
                        "entidad_clave": metric,
                        "valor": float(val),
                        "unidad": "Personas",
                        "periodo": anio,
                    }
                )
        return records
